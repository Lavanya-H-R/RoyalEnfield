/**
 *---------
 *  @brief     : Function calc_crc32 calculates the crc32 for the continuous memory block of 
 *               given size.It calculates the new crc32 value based on the older crc32 value.
 *
 *  @param     : Crc_Data_pu8 - The starting address of the memory block
 *              Crc_Size_u32 - size of the memory block
 *              Crc_Last_Crc_su32 - previous value of crc32
 *
 *  @return   : The crc32 value calculated - It returns calculated crc32 value.
 *  
 *  @code tag : @[CODE_CRC32_FUNC_2] 
 *              @{SDD_CRC32_FUNC_2}
 *-----------
 */
uint32_t Crc_CalculateCRC32(const uint8_t *Crc_Data_pu8, uint32_t Crc_Size_u32, uint32_t Crc_Last_Crc32_u32)
{    
    /* Continue the CRC calculation for SIZE_PER_ATTEMPT. SIZE_PER_ATTEMPT is configurable and
       and the crc will be calculated for the specified SIZE_PER_ATTEMPT at a time.*/
    for (;Crc_Size_u32 > 0; Crc_Size_u32--)
    {
        /* calculate the CRC by using the look up table and the previous calculated CRC32.
           For the first time the previous value will be 0xFFFFFFFF */
        /* Calculating the CRC by EX-OR the lookup table value and 24-bit of last calculated CRC32 value*/
        Crc_Last_Crc32_u32 = (((Crc_Last_Crc32_u32>> 8) & 0x00FFFFFF) ^ 
        /* As the lookup table is 256 i.e) Means it can be refer only by 8-bit identification.*/
        /* Finding the 8-bit reference value from last calculated crc and 8-bit data to access the table.*/
        CRC32_Lookup_Table_au32[(Crc_Last_Crc32_u32 ^ *Crc_Data_pu8++) & 0x000000FF]);
    }
    /* return the calculated result */   
    return Crc_Last_Crc32_u32;
}

/**
 * @brief
 *
 * @param Crc_Data_pu8
 * @param size
 * @return uint8_t
 */
uint8_t Crc_CalculateCRC8 (uint8_t *Crc_Data_pu8, uint32_t size)
{
    uint32_t CRC_index = 0;
    uint8_t crc = 0;

    if (size > 0)
    {
        for ( CRC_index = 0; CRC_index < size; CRC_index++)
        {
            crc = CRC8_Lookup_Table_au8[crc ^ Crc_Data_pu8[CRC_index]];
        }
    }
    return crc;
}


